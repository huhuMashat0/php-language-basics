<?php
   $num1 = 6;
   $num2 = 3;

   echo "{$num1} + {$num2} = " . $num1 + $num2 . "<br>";
   echo "{$num1} - {$num2} = " . $num1 - $num2 . "<br>";
   echo "{$num1} / {$num2} = " . $num1 / $num2 . "<br>";
   echo "{$num1} * {$num2} = " . $num1 * $num2 . "<br>";
   echo "{$num1} ** {$num2} = " . $num1 ** $num2 . "<br>";
   echo "{$num1} % {$num2} = " . $num1  % $num2 . "<br>";

   echo "{$num1}++ = " . $num1++ . "<br>";
   echo "--{$num2} = " . --$num2 . "<br>";
?>